# for loop
a=[] 
n=int(input("enter the number"))
for i in range(n):
    x=input("enter item to add in the list")
    a.append(x)
    print(a)
print(a)


# while loop 
a=[]
n=int(input("enter the number"))
i=0
while i<=n:
    x=input("enter item to add in the list")
    a.append(x)
    print(a)
    i=i+1
print(a)
